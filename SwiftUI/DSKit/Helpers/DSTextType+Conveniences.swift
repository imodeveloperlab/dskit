public extension DSTextType {

    static var smallTitle: DSTextType {
        .fontAndSize(.headline, 14)
    }
    
    static var smallSubtitle: DSTextType {
        .fontAndSize(.subheadline, 12)
    }
    
    static var bigTitle: DSTextType {
        .fontAndSize(.headline, 30)
    }
}
